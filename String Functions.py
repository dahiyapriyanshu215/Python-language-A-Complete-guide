str = "i am a coder"

# endswith()
print(str.endswith("der"))
print(str.endswith("age"))

# capitalize()
print(str.capitalize())

# replace()
print(str.replace("a","p"))

# find()
print(str.find("o"))
print(str.find("a"))
print(str.find("am"))

# count()
print(str.count("o"))
print(str.count("a"))