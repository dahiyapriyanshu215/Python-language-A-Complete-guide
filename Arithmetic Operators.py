a = 10
b = 2

# Sum of two numbers
sum = a + b
print(sum)

# Difference of two numbers
diff = a - b
print(diff)

# Multiplication of two numbers
mul = a * b
print(mul)

# Division of two numbers
div = a / b
print(div)

# Modulus of two numbers
mod = a % b
print(mod)

# Power of two numbers
pow = a ** b
print(pow)